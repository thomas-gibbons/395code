void process_buf(uint16_t offset) {
	for (uint16_t i = 0; i < ABUF_SIZE/2; i++) {
		i+=offset;
		aout[i] = ((ain[i] & 0x0000FFFF) << 8) | ((ain[i] & 0xFF000000) >> 24);
		process_audio();
		dataI2S[i] = aout[i];
		aout[i] = ((aout[i] & 0x00FFFF00) >> 8) | ((aout[i] & 0xFF) << 24);
		i-=offset;
	}
}

